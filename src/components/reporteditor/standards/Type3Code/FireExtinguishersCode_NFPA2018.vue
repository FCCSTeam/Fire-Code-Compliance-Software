      <h2>Chapter 7 Inspection, Maintenance, and Recharging</h2>
      <h5>
        7.1 *  General. <br />
        7.1.1 Responsibility.
      </h5>
      <p>
        The owner or designated agent or occupant of a property in which fire
        extinguishers are located shall be responsible for inspection,
        maintenance, and recharging. (See 7.1.2.)
      </p>

      <h5>7.1.2 Personnel.</h5>
      <h5>7.1.2.1 *</h5>
      <p>
        Persons performing maintenance and recharging of extinguishers shall be
        certified.
      </p>

      <h5>7.1.2.1.1</h5>
      <p>
        Persons training to become certified shall be permitted to perform
        maintenance and recharging of extinguishers under the direct supervision
        and in the immediate presence of a certified person.
      </p>

      <h5>7.1.2.1.2 *</h5>
      <p>
        Certification requires that a person pass a test administered by an
        organization acceptable to the AHJ.
      </p>

      <h5>7.1.2.1.3</h5>
      <p>
        The test shall, at a minimum, be based upon knowledge of the chapters
        and annexes of this standard.
      </p>

      <h5>7.1.2.1.4</h5>
      <p>
        The testing process shall permit persons to use the standard during the
        test.
      </p>

      <h5>7.1.2.1.5</h5>
      <p>
        Persons passing the test required in 7.1.2.1.2 shall be issued a
        document or a certificate.
      </p>

      <h5>7.1.2.1.6</h5>
      <p>
        The document or certificate shall be made available when requested by
        the authority having jurisdiction.
      </p>

      <h5>7.1.2.2</h5>
      <p>
        Persons performing maintenance and recharging of extinguishers shall be
        trained and shall have available the appropriate manufacturer’s
        servicing manual(s), the correct tools, recharge materials, lubricants,
        and manufacturer’s replacement parts or parts specifically listed for
        use in the fire extinguisher.
      </p>

      <h5>7.1.2.3 *</h5>
      <p>
        Persons performing inspections shall not be required to be certified.
      </p>

      <h5>7.1.3 Replacement While Servicing.</h5>
      <p>
        Fire extinguishers removed from service for maintenance or recharging
        shall be replaced by a fire extinguisher suitable for the type of hazard
        being protected and shall be of at least equal rating.
      </p>

      <h5>7.1.4 Tags or Labels.</h5>
      <h5>7.1.4.1</h5>
      <p>
        Tags or labels intended for recording inspections, maintenance, or
        recharging shall be affixed so as not to obstruct the fire extinguisher
        use, fire extinguisher classification, or manufacturer’s labels.
      </p>

      <h5>7.1.4.2</h5>
      <p>
        Labels indicating fire extinguisher use or classification, or both,
        shall be permitted to be placed on the front of the fire extinguisher.
      </p>

      <h5>7.2 Inspection.</h5>
      <h5>7.2.1 Inspection Frequency.</h5>
      <h5>7.2.1.1 *</h5>
      <p>
        Fire extinguishers shall be manually inspected when initially placed in
        service.
      </p>

      <h5>7.2.1.2 *</h5>
      <p>
        Fire extinguishers and Class D extinguishing agents shall be inspected
        either manually or by means of an electronic monitoring device/system at
        intervals not exceeding 31 days.
      </p>

      <h5>7.2.1.2.1</h5>
      <p>
        Fire extinguishers and Class D extinguishing agents shall be inspected
        at least once per calendar month.
      </p>

      <h5>7.2.1.3 *</h5>
      <p>
        Fire extinguishers and Class D extinguishing agents shall be manually
        inspected daily or weekly when conditions exist that indicate the need
        for more frequent inspections.
      </p>

      <h5>7.2.1.4</h5>
      <p>
        Extinguishers that are electronically monitored for location only, such
        as those monitored by means of a switch to indicate when the
        extinguisher is removed from its bracket or cabinet, shall be manually
        inspected in accordance with 7.2.2.
      </p>

      <h5>7.2.2 Inspection Procedures.</h5>
      <p>
        Periodic inspection or electronic monitoring of fire extinguishers shall
        include a check of at least the following items: 1 Location in
        designated place 2 Visibility of the extinguisher or means of indicating
        the extinguisher location 3 Access to the extinguisher 4 Pressure gauge
        reading or indicator in the operable range or position 5 Fullness
        determined by weighing or hefting 6 Condition of tires, wheels,
        carriage, hose, and nozzle for wheeled extinguishers 7 Indicator for
        nonrechargeable extinguishers using push-to-test pressure indicators
      </p>

      <h5>7.2.2.1</h5>
      <p>
        The owner or the owner’s agent shall determine the method of
        extinguisher inspection such as manual inspection, electronic
        monitoring, or any combination of the two.
      </p>

      <h5>7.2.2.2</h5>
      <p>
        Any method(s) of inspection other than manual inspection shall require
        the approval of the authority having jurisdiction.
      </p>

      <h5>7.2.2.3 *</h5>
      <p>
        In addition to 7.2.2, fire extinguishers shall be visually inspected in
        accordance with 7.2.2.4 if they are located where any of the following
        conditions exists: 1 High frequency of fires in the past 2 Severe
        hazards 3 Locations that make fire extinguishers susceptible to
        mechanical injury or physical damage 4 Exposure to abnormal temperatures
        or corrosive atmospheres
      </p>

      <h5>7.2.2.4</h5>
      <p>
        Where required by 7.2.2.3, the following inspection procedures shall be
        in addition to those addressed in 7.2.2: 1 Verify that operating
        instructions on nameplates are legible and face outward 2 Check for
        broken or missing safety seals and tamper indicators 3 Examine for
        obvious physical damage, corrosion, leakage, or clogged nozzle
      </p>

      <h5>
        7.2.2.5 Inspection Procedure for Containers of Class D Extinguishing
        Agent.
      </h5>
      <p>
        Periodic inspection of containers of Class D extinguishing agent used to
        protect Class D hazards shall include verification of at least the
        following: 1 Located in designated place 2 Visibility of the container
        or means of indicating the container location 3 Access to the container
        4 Lid is sealed 5 Fullness by hefting or weighing 6 No obvious physical
        damage to container
      </p>

      <h5>7.2.3 Corrective Action.</h5>
      <p>
        When an inspection of any fire extinguisher reveals a deficiency in any
        of the conditions in 7.2.2 or 7.2.2.4, immediate corrective action shall
        be taken.
      </p>

      <h5>7.2.3.1 Rechargeable Fire Extinguishers.</h5>
      <p>
        When an inspection of any rechargeable fire extinguisher reveals a
        deficiency in any of the conditions in 7.2.2(4), 7.2.2(5), 7.2.2(7),
        or 7.2.2.4(1) through 7.2.2.4(3), the extinguisher shall be subjected to
        applicable maintenance procedures.
      </p>

      <h5>7.2.3.2 Nonrechargeable Dry Chemical Fire Extinguisher.</h5>
      <p>
        When an inspection of any nonrechargeable dry chemical fire extinguisher
        reveals a deficiency in any of the conditions listed
        in 7.2.2(4), 7.2.2(5), 7.2.2(7), or 7.2.2.4(1) through 7.2.2.4(3), the
        extinguisher shall be removed from further use, discharged, and
        destroyed at the direction of the owner or returned to the manufacturer.
      </p>

      <h5>7.2.3.3 Nonrechargeable Halon Agent Fire Extinguisher.</h5>
      <p>
        When an inspection of any nonrechargeable fire extinguisher containing a
        halon agent reveals a deficiency in any of the conditions listed
        in 7.2.2(4), 7.2.2(5), 7.2.2(7), or 7.2.2.4(1) through 7.2.2.4(3), the
        extinguisher shall be removed from service, shall not be discharged, and
        shall be returned to the manufacturer, a fire equipment dealer, or a
        distributor to permit recovery of the halon.
      </p>

      <h5>7.2.4 Inspection Record Keeping.</h5>
      <h5>7.2.4.1 Manual Inspection Records.</h5>
      <h5>7.2.4.1.1</h5>
      <p>
        Where manual inspections are conducted, records for manual inspections
        shall be kept on a tag or label attached to the fire extinguisher, on an
        inspection checklist maintained on file, or by an electronic method.
      </p>

      <h5>7.2.4.1.2</h5>
      <p>
        Where manual inspections are conducted, the month and year the manual
        inspection was performed and the initials of the person performing the
        inspection shall be recorded.
      </p>

      <h5>7.2.4.1.3</h5>
      <p>
        Personnel making manual inspections shall keep records of all fire
        extinguishers inspected, including those found to require corrective
        action.
      </p>

      <h5>7.2.4.1.4</h5>
      <p>
        Records for manual inspection shall be kept to demonstrate that at least
        the last 12 monthly inspections have been performed.
      </p>

      <h5>7.2.4.2 Electronic Inspection Records.</h5>
      <h5>7.2.4.2.1</h5>
      <p>
        Where electronically monitored systems are employed for inspections,
        records shall be kept for fire extinguishers found to require corrective
        action.
      </p>

      <h5>7.2.4.2.2</h5>
      <p>
        Records for electronic monitoring shall be kept to demonstrate that at
        least the last 12 monthly inspections have been performed.
      </p>

      <h5>7.2.4.2.3</h5>
      <p>
        For electronically monitored fire extinguishers, where the extinguisher
        causes a signal at a control unit when a deficiency in any of the
        conditions listed in 7.2.2occurs, record keeping shall be provided in
        the form of an electronic event log at the control panel.
      </p>

      <h5>7.3 Extinguisher Maintenance.</h5>
      <h5>7.3.1 *  Maintenance Procedures.</h5>
      <p>
        Where required by another section of this standard, maintenance
        procedures shall include the procedures detailed in the manufacturer’s
        service manual and a thorough examination of the basic elements of the
        fire extinguisher, including the following: 1 Mechanical parts of all
        fire extinguishers 2 Extinguishing agent 3 Expelling means 4 Physical
        condition
      </p>

      <h5>7.3.1.1</h5>
      <p>
        Fire extinguishers shall be subjected to maintenance at intervals of not
        more than 1 year, at the time of hydrostatic test, or when specifically
        indicated by an inspection discrepancy or electronic notification.
      </p>

      <h5>7.3.2 Annual External Examination of All Extinguishers.</h5>
      <h5>7.3.2.1 Physical Condition.</h5>
      <p>
        An annual external visual examination of all fire extinguishers shall be
        made to detect obvious physical damage, corrosion, or nozzle blockage to
        verify that the operating instructions are present, legible, and facing
        forward, and that the HMIS information is present and legible, and to
        determine if a 6-year interval examination or hydrostatic test is due.
      </p>

      <h5>7.3.2.2 *  Seals or Tamper Indicators.</h5>
      <p>
        At the time of the maintenance, the tamper seal of a rechargeable fire
        extinguisher shall be removed by operating the pull pin or locking
        device.
      </p>

      <h5>7.3.2.2.1</h5>
      <p>
        After the applicable maintenance procedures are completed, a new listed
        tamper seal shall be installed.
      </p>

      <h5>7.3.2.2.2</h5>
      <p>
        Seals or tamper indicators on nonrechargeable-type extinguishers shall
        not be removed.
      </p>

      <h5>7.3.2.3 *  Boots, Foot Rings, and Attachments.</h5>
      <p>
        All removable extinguisher boots, foot rings, and attachments shall be
        removed to accommodate thorough annual cylinder examinations.
      </p>

      <h5>7.3.2.4</h5>
      <p>
        When subjected to temperatures at or above their listed rating,
        stored-pressure fire extinguishers that require a 12-year hydrostatic
        test shall be emptied and subjected to the applicable maintenance and
        recharge procedures on an annual basis.
      </p>

      <h5>7.3.2.5 Corrective Action.</h5>
      <p>
        When an external examination of any fire extinguisher reveals a
        deficiency, immediate corrective action shall be taken.
      </p>

