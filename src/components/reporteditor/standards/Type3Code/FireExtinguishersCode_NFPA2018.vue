      <h5> 7.3.3.2 Loaded Stream Charge. </h5>
Stored-pressure types of fire extinguishers containing a loaded stream agent shall be disassembled on an annual basis and subjected to complete maintenance.

<h5>7.3.3.2.1 </h5>
The loaded stream charge shall be permitted to be recovered and re-used, provided it is subjected to agent analysis in accordance with the extinguisher manufacturer’s instructions.

<h5>7.3.3.2.2 </h5>
When the internal maintenance procedures are performed during periodic recharging or hydrostatic testing, the 1-year requirement shall begin from that date.

<h5>7.3.3.3 *  Cartridge- or Cylinder-Operated Extinguishers.</h5>
The extinguishing agent of cartridge- or cylinder-operated extinguishers shall be internally examined annually.

<h5>7.3.3.4 Wetting Agent Extinguishers.</h5>
Wetting agent extinguishers shall be disassembled on an annual basis and subjected to complete maintenance.

<h5>7.3.3.5 Pump Tank Extinguishers.</h5>
Pump tank extinguishers shall be internally examined annually.

<h5>7.3.3.6 </h5>
Annual internal examination shall not be required for nonrechargeable fire extinguishers, carbon dioxide fire extinguishers, or stored-pressure fire extinguishers, except for those types specified in 7.3.3.2.

<h5>7.3.4 *  Annual Maintenance Record Keeping.</h5>
<h5>7.3.4.1 </h5>
Each fire extinguisher shall have a tag or label securely attached that indicates that maintenance was performed.

<h5>7.3.4.1.1 </h5>
The tag or label, as a minimum, shall identify the following:

1  Month and year maintenance was performed

2  Person performing the work

3  Name of the agency performing the work


<h5>7.3.4.2 </h5>
Each extinguisher that has undergone maintenance that includes internal examination, except extinguishers identified in 7.3.3.3 and 7.3.3.5 shall have a verification-of-service collar located around the neck of the container.

<h5>7.3.5 Corrective Action.</h5>
When maintenance of any fire extinguisher reveals a deficiency, immediate corrective action shall be taken.

<h5> 7.3.6 Six-Year Internal Examination of Certain Types of Extinguishers.</h5>
Every 6 years, stored-pressure fire extinguishers that require a 12-year hydrostatic test shall be emptied and subjected to the applicable internal and external examination procedures as detailed in the manufacturer’s service manual and this standard.

<h5> 7.3.6.1 </h5>
When the applicable maintenance procedures are performed during periodic recharging or hydrostatic testing, the 6-year requirement shall begin from that date.

<h5> 7.3.6.2 * </h5>
The removal of agent from halon agent fire extinguishers shall only be done using a listed halon closed recovery system.

<h5> 7.3.6.3 </h5>
Nonrechargeable fire extinguishers shall not be required to have a 6-year internal examination and shall not be hydrostatically tested but shall be removed from service at a maximum interval of 12 years from the date of manufacture.

<h5> 7.3.6.3.1 </h5>
Nonrechargeable halon agent fire extinguishers shall be disposed of in accordance with 7.2.3.3.

<h5> 7.3.6.4 Corrective Action.</h5>
When an internal examination of any fire extinguisher reveals a deficiency, immediate corrective action shall be taken.

<h5> 7.3.6.5 *  Six-Year Internal Examination Label.</h5>
Fire extinguishers that pass the applicable 6-year requirement of 7.3.6 shall have the maintenance information recorded on a durable weatherproof label that is a minimum of 2 in. × 31∕ 2 in. (51 mm × 89 mm).

<h5> 7.3.6.5.1 </h5>
The new label shall be affixed to the shell by a heatless process, and any previous 6-year internal examination labels shall be removed.

<h5> 7.3.6.5.2 </h5>
These labels shall be of the self-destructive type when their removal from a fire extinguisher is attempted.

<h5> 7.3.6.5.3 </h5>
The 6-year internal examination label shall, as a minimum, identify the following:

1  Month and year the 6-year internal examination was performed

2  Person performing the work

3  Name of the agency performing the work


<h5> 7.4 *  Carbon Dioxide Hose Assembly Conductivity Test.</h5>
A conductivity test shall be conducted annually on all carbon dioxide hose assemblies.

<h5> 7.4.1 </h5>
Carbon dioxide hose assemblies that fail the conductivity test shall be replaced.

<h5> 7.4.2 Record Keeping for Conductivity Testing of Carbon Dioxide Hose Assemblies.</h5>
<h5> 7.4.2.1 </h5>
Carbon dioxide hose assemblies that pass a conductivity test shall have the test information recorded on a durable weatherproof label that is a minimum of 1∕ 2in. × 3 in. (13 mm × 76 mm).

<h5> 7.4.2.2 </h5>
The label shall be affixed to the hose by means of a heatless process.

<h5> 7.4.2.3 </h5>
The label shall include the following information:

1  Month and year the test was performed, indicated by perforation, such as is done by a hand punch

2  Name or initials of person performing the test and the name of the agency performing the test


<h5> 7.5 Hose Station Maintenance.</h5>
Where hose stations are installed to comply with 6.2.1.4, they shall be maintained in accordance with NFPA 1962.

<h5> 7.6 Electronic Monitoring System Maintenance.</h5>
<h5> 7.6.1 Electronic Monitoring.</h5>
The components of the monitoring device/system shall be tested and maintained annually in accordance with the manufacturer’s listed maintenance manual, with the following items included as a minimum:

1  Power supply inspection/battery change

2  Obstruction sensor inspection

3  Location sensor inspection

4  Pressure indication inspection

5  Connection continuity inspection (See 7.6.1.1 and 7.6.1.2.)


<h5> 7.6.1.1 </h5>
One hundred percent of all units shall be tested upon initial installation or reacceptance with verification of receipt of signal at the control panel or a local alarm.

<h5> 7.6.1.2 </h5>
Twenty percent of units shall be tested annually on a rotating basis so that all units are tested within a 5-year period.

<h5> 7.6.2 </h5>
When used in conjunction with fire alarm systems, fire extinguisher electronic monitoring devices shall be inspected and maintained in accordance with NFPA 72 and 7.6.1.

<h5> 7.6.3 Corrective Action.</h5>
When maintenance of any monitoring system reveals a deficiency, immediate corrective action shall be taken.

<h5> 7.7 Maintenance of Wheeled Extinguisher Hoses and Regulators.</h5>
<h5> 7.7.1 Wheeled Unit Hoses.</h5>
Discharge hoses on wheeled-type fire extinguishers shall be completely uncoiled and examined for damage annually.

<h5> 7.7.1.1 * </h5>
Discharge hoses on wheeled extinguishers shall be coiled in a manner to prevent kinks and to allow rapid deployment in accordance with the manufacturer’s instructions.

<h5> 7.7.2 Pressure Regulators.</h5>
Pressure regulators provided with wheeled-type fire extinguishers shall be tested annually for outlet static pressure and flow rate in accordance with the manufacturer’s instructions.

<h5> 7.7.3 Corrective Action.</h5>
When maintenance of any fire extinguisher hose or pressure regulator reveals a deficiency, immediate corrective action shall be taken.

<h5> 7.8 Extinguisher Recharging and Extinguishing Agents.</h5>
<h5> 7.8.1 *  General.</h5>
<h5> 7.8.1.1 </h5>
All rechargeable-type fire extinguishers shall be recharged after any use or when the need is indicated by an inspection or servicing.

<h5> 7.8.1.2 * </h5>
When recharging is performed, the manufacturer’s service manual shall be followed. (For recharge agents, see 7.8.3.)

<h5> 7.8.1.3 * </h5>
The amount of recharge agent shall be verified by weighing.

<h5> 7.8.1.3.1 </h5>
For those fire extinguishers that do not have the gross weight marked on the nameplate or valve, a permanent label that indicates the gross weight shall be affixed to the cylinder.

<h5> 7.8.1.3.2 </h5>
The added label containing the gross weight shall be a durable material of a pressure-sensitive, self-destruct type. (For stored-pressure water-type extinguishers, see 7.8.3.10.)

<h5> 7.8.1.3.3 </h5>
Pump tank water and pump tank calcium chloride–based antifreeze types shall not be required to have weight marked.

<h5> 7.8.1.3.4 * </h5>
After recharging, a leak test shall be performed on stored-pressure and self-expelling types of fire extinguishers.

<h5> 7.8.1.3.5 </h5>
In no case shall an extinguisher be recharged without hydrostatic testing if it is beyond its specified hydrostatic test date.

<h5> 7.8.2 Extinguisher Recharging Frequency for Certain Types of Extinguishers.</h5>
<h5> 7.8.2.1 Pump Tank.</h5>
Every 12 months, pump tank water and pump tank calcium chloride–based antifreeze types of fire extinguishers shall be recharged with new chemicals or water as applicable.

<h5> 7.8.2.2 Wetting Agent.</h5>
The agent in stored-pressure wetting agent fire extinguishers shall be replaced annually.

<h5> 7.8.2.2.1 </h5>
Only the agent specified on the nameplate shall be used for recharging.

<h5> 7.8.2.2.2 </h5>
The use of water or any other additives shall be prohibited.

<h5> 7.8.2.3 AFFF and FFFP.</h5>
<h5> 7.8.2.3.1 </h5>
The premixed agent in liquid charge–type AFFF and FFFP fire extinguishers shall be replaced at least once every 3 years.

<h5> 7.8.2.3.2 </h5>
Only the foam agent specified on the extinguisher nameplate shall be used for recharge.

<h5> 7.8.2.3.3 </h5>
The agent in nonpressurized AFFF and FFFP fire extinguishers that is subjected to agent analysis in accordance with manufacturer’s instructions shall not be required to comply with 7.8.2.3.1.

<h5> 7.8.3 *  Recharge Agents.</h5>
<h5> 7.8.3.1 </h5>
Only those agents specified on the nameplate or agents proven to have equal chemical composition, physical characteristics, and fire-extinguishing capabilities shall be used.

<h5> 7.8.3.1.1 </h5>
Agents listed specifically for use with that fire extinguisher shall be considered to meet these requirements.

<h5> 7.8.3.2 *  Mixing of Dry Chemicals.</h5>
Multipurpose dry chemicals shall not be mixed with alkaline-based dry chemicals.

<h5> 7.8.3.3 Topping Off.</h5>
<h5> 7.8.3.3.1 </h5>
The remaining dry chemical in a discharged fire extinguisher shall be permitted to be re-used, provided that it is thoroughly checked for the proper type, contamination, and condition.

<h5> 7.8.3.3.2 </h5>
Dry chemical found to be of the wrong type or contaminated shall not be re-used.

<h5> 7.8.3.4 Dry Chemical Agent Re-Use.</h5>
<h5> 7.8.3.4.1 </h5>
The dry chemical agent shall be permitted to be re-used, provided a closed recovery system is used and the agent is stored in a sealed container to prevent contamination.

<h5> 7.8.3.4.2 </h5>
Prior to re-use, the dry chemical shall be thoroughly checked for the proper type, contamination, and condition.

<h5> 7.8.3.4.3 </h5>
Where doubt exists with respect to the type, contamination, or condition of the dry chemical, the dry chemical shall be discarded.

<h5> 7.8.3.4.4 Dry Chemical Closed Recovery System.</h5>
<h5> 7.8.3.4.4.1 </h5>
The system shall be constructed in a manner that does not introduce foreign material into the agent being recovered.

<h5> 7.8.3.4.4.2 </h5>
The system shall have a means for visual inspection of the recovered agent for contaminants.

<h5> 7.8.3.5 Dry Powder.</h5>
<h5> 7.8.3.5.1 </h5>
Pails or drums containing dry powder agents for scoop or shovel application for use on metal fires shall be kept full and sealed with the lid provided with the container.

<h5> 7.8.3.5.2 </h5>
The dry powder shall be replaced if found damp. (See A.‍7.8.3.)

<h5> 7.8.3.6 *  Removal of Moisture.</h5>
For all non-water types of fire extinguishers, any moisture shall be removed before recharging.

<h5> 7.8.3.7 *  Halogenated Agent.</h5>
Halogenated agent fire extinguishers shall be charged with only the type and weight of agent specified on the nameplate.

<h5> 7.8.3.8 Halogenated Agent Re-Use.</h5>
<h5> 7.8.3.8.1 </h5>
The removal of Halon 1211 from fire extinguishers shall be done using only a listed halon closed recovery system.

<h5> 7.8.3.8.2 </h5>
The removal of agent from other halogenated agent fire extinguishers shall be done using only a closed recovery system.

<h5> 7.8.3.8.3 </h5>
The fire extinguisher shall be examined internally for contamination or corrosion or both.

<h5> 7.8.3.8.4 </h5>
The halogenated agent retained in the system recovery cylinder shall be re-used only if no evidence of internal contamination is observed in the fire extinguisher cylinder.

<h5> 7.8.3.8.5 </h5>
Halogenated agent removed from fire extinguishers that exhibits evidence of internal contamination or corrosion shall be processed in accordance with the fire extinguisher manufacturer’s instructions.

<h5> 7.8.3.9 *  Carbon Dioxide.</h5>
<h5> 7.8.3.9.1 </h5>
The vapor phase of carbon dioxide shall be not less than 99.5 percent carbon dioxide.

<h5> 7.8.3.9.2 </h5>
The water content shall be not more than 60 parts per million (ppm) by weight at −52°F (−47°C) dew point.

<h5> 7.8.3.9.3 </h5>
Oil content shall not exceed 10 ppm by weight.

<h5> 7.8.3.10 *  Water Types.</h5>
The amount of liquid agent shall be determined by using one of the following:

1  Exact measurement by weight

2  Exact measurement by volume

3  Anti-overfill tube, if provided

4  Fill mark on fire extinguisher shell, if provided


<h5> 7.8.3.10.1 </h5>
Only the agent specified on the extinguisher nameplate shall be used for recharge.

<h5> 7.8.3.10.2 </h5>
Only additives identified on the original nameplate shall be permitted to be added to water-type extinguishers.

<h5> 7.8.3.11 Wet Chemical and Water Mist Agent Re-Use.</h5>
<h5> 7.8.3.11.1 </h5>
Wet chemical and water mist agents shall not be re-used.

<h5> 7.8.3.11.2 </h5>
If a wet chemical or water mist extinguisher is partially discharged, all remaining wet chemical or water mist shall be discarded.

<h5> 7.8.3.11.3 </h5>
Wet chemical or water mist agent shall be discarded and replaced at the hydrostatic test interval.

<h5> 7.8.3.11.3.1 </h5>
Only the agent specified on the extinguisher nameplate shall be used for recharge.

<h5> 7.8.4 Recharging Expellant Gas for Stored-Pressure Fire Extinguishers.</h5>
<h5> 7.8.4.1 </h5>
Only standard industrial-grade nitrogen with a maximum dew point of −60°F (−51°C), in accordance with CGA G-10.1, Commodity Specification for Nitrogen, shall be used to pressurize stored-pressure dry chemical and halogenated-type fire extinguishers that use nitrogen as a propellant.

<h5> 7.8.4.2 </h5>
Halogenated-type fire extinguishers that require argon shall be pressurized with argon with a dew point of −65°F (−54°C) or lower.

<h5> 7.8.4.3 </h5>
Compressed air shall be permitted to be used from special compressor systems capable of delivering air with a dew point of −60°F (−51°C) or lower. (See Annex J.)

<h5> 7.8.4.3.1 </h5>
The special compressor system shall be equipped with an automatic monitoring and alarm system to ensure that the dew point remains at or below −60°F (−51°C) at all times.

<h5> 7.8.4.3.2 </h5>
Compressed air through moisture traps shall not be used for pressurizing even though so stated in the instructions on older fire extinguishers.

<h5> 7.8.4.3.3 </h5>
Compressed air without moisture removal devices shall be permitted for pressurizing water extinguishers and foam hand extinguishers only.

<h5> 7.8.4.4 * </h5>
Class D wet chemical, water mist, and halogenated agent fire extinguishers shall be repressurized only with the type of expellant gas referred to on the fire extinguisher label.

<h5> 7.8.4.5 </h5>
A rechargeable stored-pressure-type fire extinguisher shall be pressurized only to the charging pressure specified on the fire extinguisher nameplate.

<h5> 7.8.4.5.1 </h5>
The manufacturer’s pressurizing adapter shall be connected to the valve assembly before the fire extinguisher is pressurized.

<h5> 7.8.4.5.2 </h5>
A regulated source of pressure, set no higher than 25 psi (172 kPa) above the operating (service) pressure, shall be used to pressurize fire extinguishers.

<h5> 7.8.4.5.3 </h5>
The gauge used to set the regulated source of pressure shall be calibrated at least annually.

<h5> 7.8.4.6 * </h5>
An unregulated source of pressure, such as a nitrogen cylinder without a pressure regulator, shall not be used.

<h5> 7.8.4.7 * </h5>
A fire extinguisher shall not be left connected to the regulator of a high-pressure source for an extended period of time.

<h5> 7.8.4.8 Recharge Record Keeping.</h5>
<h5> 7.8.4.8.1 </h5>
Each fire extinguisher shall have a tag or label attached that indicates the month and year recharging was performed, identifies the person performing the service, and identifies the name of the agency performing the work.

<h5> 7.8.4.8.2 </h5>
Each extinguisher that has been recharged shall have a verification-of-service collar located around the neck of the container, except as identified in 7.11.4.

<h5> 7.9 *  Pressure Gauges.</h5>
<h5> 7.9.1 </h5>
Replacement pressure gauges shall have the correct indicated charging (service) pressure.

<h5> 7.9.2 </h5>
Replacement pressure gauges shall be marked for use with the agent in the fire extinguisher.

<h5> 7.9.3 </h5>
Replacement pressure gauges shall be compatible with the fire extinguisher valve body material.

<h5> 7.10 Prohibition on Uses of Extinguishers and Conversion of Fire Extinguisher Types.</h5>
<h5> 7.10.1 </h5>
Fire extinguishers shall not be used for any purpose other than that of a fire extinguisher.

<h5> 7.10.2 </h5>
Fire extinguishers shall not be converted from one type to another, modified, or altered.

<h5> 7.10.3 </h5>
Fire extinguishers shall not be converted for the use of a different type of extinguishing agent.

<h5> 7.11 *  Maintenance and Recharge Service Collar.</h5>
Each extinguisher that has undergone maintenance that included internal examination or that has been recharged requiring the removal of the valve assembly shall have a verification-of-service collar located around the neck of the container.

<h5> 7.11.1 </h5>
The collar shall be a single circular piece of uninterrupted material forming a hole of a size that does not permit the collar assembly to move over the neck of the container unless the valve is completely removed.

<h5> 7.11.2 </h5>
The collar shall not interfere with the operation of the fire extinguisher.

<h5> 7.11.3 </h5>
The verification of service collar shall, as a minimum, identify the following:

1  Month and year the recharging or internal examination was performed

2  Name of the agency performing the work


<h5> 7.11.4 Service Collar Exemptions.</h5>
<h5> 7.11.4.1 </h5>
New extinguishers requiring an initial charge in the field (such as pressurized water, AFFF, FFFP, or wet chemical extinguishers) shall not be required to have a verification-of-service collar installed.

<h5> 7.11.4.2 </h5>
Liquefied gas, halogenated agent, and carbon dioxide extinguishers that have been recharged without valve removal shall not be required to have a verification-of-service collar installed following recharge.

<h5> 7.11.4.3 </h5>
Cartridge- and cylinder-operated extinguishers shall not be required to have a verification-of-service collar installed.

<h5> 7.12 *  Weight Scales.</h5>
Weight scales used for the maintenance and recharge of fire extinguishers shall have the reading increments and the accuracy necessary to verify the charge weights required in the service manuals and on the nameplates.

